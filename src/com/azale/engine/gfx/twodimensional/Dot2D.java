package com.azale.engine.gfx.twodimensional;

public class Dot2D {

    double x, y, w;

    public Dot2D(double givenX, double givenY) {

        x = givenX;
        y = givenY;
        w = 1;

    }

    /*

    GETTERS AND SETTERS

     */

    public double getX() {

        return x;

    }

    public double getY() {

        return y;

    }

}
