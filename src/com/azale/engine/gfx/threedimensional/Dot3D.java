package com.azale.engine.gfx.threedimensional;

public class Dot3D {

    public double x, y, z;

    public Dot3D(double givenX, double givenY, double givenZ) {

        x = givenX;
        y = givenY;
        z = givenZ;

    }

    /*

    GETTERS AND SETTERS

     */

    public double getX() {

        return x;
    }


    public double getY() {

        return y;

    }

    public double getZ() {

        return z;

    }

}
