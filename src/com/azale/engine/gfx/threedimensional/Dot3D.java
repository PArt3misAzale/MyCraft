package com.azale.engine.gfx.threedimensional;

public class Dot3D {

    public float x, y, z;

    public Dot3D(float givenX, float givenY, float givenZ) {

        x = givenX;
        y = givenY;
        z = givenZ;

    }

    /*

    GETTERS AND SETTERS

     */

    public float getX() {

        return x;
    }


    public float getY() {

        return y;

    }

    public float getZ() {

        return z;

    }

}
